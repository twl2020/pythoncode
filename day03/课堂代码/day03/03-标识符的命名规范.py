# 原则： 见名知意
# 组成： 字母，数字， _
# 大驼峰命名法  小驼峰命名法  下划线命名法
# 类： 大驼峰命名法， 每个单词的首字母大写
# 变量， 方法， 函数： 小驼峰命名法   第一个单词的首字母小写    --- 官方不推荐
# 变量， 方法， 函数： 下划线命名法   每个单词的全部小写， 单词之间使用_连接   --- 官方推荐
# 常量  全部大写， 多个单词使用_连接


