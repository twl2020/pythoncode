"""
需求：校验手机号码
  1. 必须是数字
  2. 长度必须是11位
  3. 必须是130  131  132  133 134 135 136 137  151 153 155  170 173 189 开头
"""
import re
tel = "13787613021"
# [abc|.]  [] 隐藏功能就是 可以将 正则符号变成 普通字符
# m = re.match("^(130|131|132|133|134|135|136|137|151|153|155|170|173|189)[0-9]{8}$", tel)
m = re.match("^(13[0-7]|15[135]|17[03]|189)\\d{8}$", tel)

if m:
    print("匹配成功")
else:
    print("匹配失败")

