import math

print(math.e)  # 2.718281828459045  自然底数
print(math.pi)  # 3.141592653589793  圆周率
print(math.acos(0.1))  # 反余弦
print(math.cos(90))  # 余弦
print(math.degrees(3.1))  # 角度值
print(math.factorial(5))  # 阶乘
print(math.pow(2, 3))  # 幂函数
print(math.sqrt(9))  # 开方
print(math.hypot(3, 4))  # 计算欧式距离  sqrt(sum(x**2 for x in coordinates))
print(math.fabs(-100))  # 绝对值
print(math.floor(3.14))  # 向下取整
print(math.ceil(3.14))  # 向上取整