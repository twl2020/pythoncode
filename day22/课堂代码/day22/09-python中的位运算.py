# &  按位与
print(0 & 100)  # 0
print(0 & 255)  # 0
print(0 & 0)  # 0

print(255 & 100)  # 100
print(255 & 30)  # 30

print("-----------------------------")

# | 按位或
print(0 | 100)  # 100
print(255 | 200)  # 255

print("---------------------------------")
# ^ 按位异或
print(0 ^ 100)  # 100
print(255 ^ 100)  # 155
print(3 ^ 4 ^ 3)  # 4

# ~ 按位取反
print(~0)
